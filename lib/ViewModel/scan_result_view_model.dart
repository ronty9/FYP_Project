import 'dart:io';

import '../models/scan_prediction.dart';
import '../scan_type.dart';
import '../services/ai_service.dart';
import 'base_view_model.dart';

// Re-export so existing imports keep working.
export '../models/scan_prediction.dart';

class ScanResultViewModel extends BaseViewModel {
  ScanResultViewModel({required this.scanType, required this.imageFile}) {
    _runPrediction();
  }

  final ScanType scanType;
  final File imageFile;

  List<ScanPrediction> _predictions = [];
  String _detailsTitle = '';
  String _summaryText = '';
  String _detectedSpecies = '';

  List<ScanPrediction> get predictions => _predictions;
  String get detailsTitle => _detailsTitle;
  String get summaryText => _summaryText;

  /// 'cat' | 'dog' - populated after the AI call completes.
  String get detectedSpecies => _detectedSpecies;

  /// Returns null while loading or on error.
  ScanPrediction? get topPrediction =>
      _predictions.isNotEmpty ? _predictions.first : null;

  String get topConfidencePercent =>
      ((topPrediction?.confidence ?? 0) * 100).toStringAsFixed(1);

  String get advisoryText {
    final top = topPrediction;
    if (top == null) return '';
    return scanType == ScanType.skinDisease
        ? 'Your pet looks healthy. (${top.label} - $topConfidencePercent%)\n$_summaryText'
        : 'Your pet\'s breed is most likely ${top.label} ($topConfidencePercent%).\n$_summaryText';
  }

  String get warningText =>
      'This result is generated by an AI model and may not be 100% accurate. '
      'It is not a medical diagnosis. Please consult a veterinarian for '
      'professional advice if you have any concerns about your pet\'s health.';

  void _runPrediction() {
    runAsync(() async {
      if (scanType == ScanType.breed) {
        await _runBreedPrediction();
      } else {
        // Skin-disease model not yet integrated - use static placeholder.
        _runStaticDiseasePlaceholder();
      }
    });
  }

  Future<void> _runBreedPrediction() async {
    final result = await AiService.predictBreed(imageFile);
    _detectedSpecies = result.species;
    _predictions = result.breedPredictions;
    _detailsTitle = 'Breed result';
    final top = _predictions.isNotEmpty ? _predictions.first : null;
    _summaryText = top == null
        ? 'Could not determine the breed from this photo.'
        : 'Your $_detectedSpecies\'s breed is most likely ${top.label}. '
              'This is an estimate and may not be exact, especially for '
              'mixed-breed pets.';
  }

  void _runStaticDiseasePlaceholder() {
    _detailsTitle = 'Disease details';
    _predictions = const [
      ScanPrediction(label: 'Healthy skin', confidence: 0.98),
      ScanPrediction(label: 'Mild irritation', confidence: 0.02),
    ];
    _summaryText =
        'From this photo, your pet\'s skin looks mostly healthy. If you notice '
        'redness, swelling or your pet seems uncomfortable, please consult a '
        'veterinarian.';
  }
}
